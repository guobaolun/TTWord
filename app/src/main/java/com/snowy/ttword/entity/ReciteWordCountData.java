package com.snowy.ttword.entity;

/**
 * @author guobaolun
 */
public class ReciteWordCountData {


    private int totalCount;
    private int accomplishCount;
    private int unfinishedCount;
    private int reciteWordCount;


    private int dayWordCount;
    private int monthWordCount;

    public int getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(int totalCount) {
        this.totalCount = totalCount;
    }

    public int getAccomplishCount() {
        return accomplishCount;
    }

    public void setAccomplishCount(int accomplishCount) {
        this.accomplishCount = accomplishCount;
    }

    public int getUnfinishedCount() {
        return unfinishedCount;
    }

    public void setUnfinishedCount(int unfinishedCount) {
        this.unfinishedCount = unfinishedCount;
    }

    public int getReciteWordCount() {
        return reciteWordCount;
    }

    public void setReciteWordCount(int reciteWordCount) {
        this.reciteWordCount = reciteWordCount;
    }


    public int getDayWordCount() {
        return dayWordCount;
    }

    public void setDayWordCount(int dayWordCount) {
        this.dayWordCount = dayWordCount;
    }

    public int getMonthWordCount() {
        return monthWordCount;
    }

    public void setMonthWordCount(int monthWordCount) {
        this.monthWordCount = monthWordCount;
    }
}
